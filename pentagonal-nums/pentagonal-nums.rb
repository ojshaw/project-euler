# Accepts an integer>0 and determines whether or not is is a pentagonal number
# Returns true or false
def is_pentnum?(n)
	n = (math.sqrt(24*n + 1) + 1 ) / 6.0
	return true if n % 1==0 else return false
end

# Accepts an integer>0 and returns the pentagonal number generated by formula 
# p_n = n(3n-1)/2
def pentnum(n)
	return n*(3*n - 1)/2
end

